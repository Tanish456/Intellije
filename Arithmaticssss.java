package basics;

public class Arithmaticssss {
        public static void main(String[] args) {
            int a = 5;
            int b = 8;
            int sum = a+b;
            System.out.println(sum);
            System.out.println(a+b);
            System.out.println(a-b);
            System.out.println(a*b);
            System.out.println(a/b);
            System.out.println(a%b);
            System.out.println(a==b);
            System.out.println(a>=b);
            System.out.println(a<=b);  //Arithmatic

            int n =5;
            System.out.println(n++);     //pre increment
            System.out.println(++n);    //post increment


            int c = 6;
            c*= 6;
            System.out.println(c); //Assignment operator

            System.out.println(6==8);  //if one condition is true whole condition is true
            System.out.println(7<=9);
            System.out.println(5==5);    //comparision operator

            int m =6,j =7,l =8;
            System.out.println(6<8 && 8<7 ); //If one condition is false the whole condition is false in end operator.
            System.out.println(6>7 || 7<8); //If one condition is true the whole condition is true in or operator.
            //LOGICAL OPERATORS.

        }
    }


