package basics;

public class while_doloops {
    public static void main(String[] args) {
        int num =2;
        while(num<=10){
            System.out.println(2*num);
            num++;}

        int sum = 3;
        while(sum<=15){
            System.out.println(sum*2-1);
            sum++;}                             //While loops

        int x =2;
        do {
            System.out.println(x);
            x++;}
        while(x<=14);                         //Do while
    }
}


